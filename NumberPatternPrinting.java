public class NumberPatternPrinting {
public static void main(String[]args){
    //Outer loop for row
    for(int i=1;i<=5;i++){
        //Inner loop for column
        for(int j=1;j<=i;j++){
        System.out.print(i);
    //Print the current row number
    }
      System.out.println();
      //Move to the next line after each row
    }
}    
}
